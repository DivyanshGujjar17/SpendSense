package com.MoneyManager.SpendSense.Service;

import com.MoneyManager.SpendSense.Dto.ExpenseDTO;
import com.MoneyManager.SpendSense.Entity.ProfileEntity;
import com.MoneyManager.SpendSense.Repositiory.ExpenseRepository;
import com.MoneyManager.SpendSense.Repositiory.ProfileRepository;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.scheduling.annotation.Scheduled;
import org.springframework.stereotype.Service;

import java.time.LocalDate;
import java.time.ZoneId;
import java.util.List;

@Service
@RequiredArgsConstructor
@Slf4j
public class NotificationService {
    private final ProfileRepository profileRepository;
    private final EmailService emailService;
    private final ExpenseService expenseService;
@Value("${money.manager.frontend.url}")
    private String frontendUrl;
@Scheduled(cron = "0 0 22 * * * ",zone = "IST")
public void sendDailyIncomeExpenseReminder(){
    log.info("job Started:sendDailyIncomeExpenseReminder()");
    List<ProfileEntity> profiles=profileRepository.findAll();
    for(ProfileEntity profile:profiles){
    String body="Hi"+profile.getFullName()+",<br><br>"+
            "This is a Friendly reminder to add your income and expense for today in money Manager.<br><br>"+
            "<a href="+frontendUrl+" style='display:inline-block;padding:10px 20px;background-color:#4CAF50;color:#fff;text-decoration:none;border-radius:5px;font-weight:bold;'> GO to Your Money Manager </a>"
            +"<br></br>Best Regards,<br> Money Manager Team";
    emailService.sendEmail(profile.getEmail(),"Daily Reminder: Add your Income and Expense",body);
    }
}
    @Scheduled(cron = "0 0 23 * * * ",zone = "IST")
public void sendDailyExpenseSummary(){
    log.info("job Started: sendDailyIncomeExpenseReminder()");
    List<ProfileEntity> profiles=profileRepository.findAll();
    for(ProfileEntity profile:profiles){
List<ExpenseDTO> todaysExpenses=expenseService.getExpenseForUserOnDate(profile.getId(), LocalDate.now());
if(!todaysExpenses.isEmpty()){
    StringBuilder table=new StringBuilder();
    table.append("<table style='border-collapse:collapse;width:100%;'>");
    table.append("<tr style='background-color:#f2f2f2;'><th style='border:1px solid #ddd;padding:8px;'>S.NO</th><th style='border:1px solid #ddd;padding:8px;'>Name</th><th style='border:1px solid #ddd;padding:8px;'>Amount</th><th style='border:1px solid #ddd;padding:8px;'>Category</th><th style='border:1px solid #ddd;padding:8px;'>Date</th></tr>");
int i=1;
for(ExpenseDTO expenseDTO:todaysExpenses){
    table.append("<tr>");
    table.append("<td style='border:1px solid #ddd;padding:8px;'>").append(i++).append("</td>");
    table.append("<td style='border:1px solid #ddd;padding:8px;'>").append(expenseDTO.getName()).append("</td>");
    table.append("<td style='border:1px solid #ddd;padding:8px;'>").append(expenseDTO.getAmount()).append("</td>");
    table.append("<td style='border:1px solid #ddd;padding:8px;'>").append(expenseDTO.getCategoryId()!=null ?expenseDTO.getCategoryName():"N/A").append("</td>");
}
table.append("</table>");
String body="Hi"+profile.getFullName()+",<br/><br/> here is a summary of Your Expense for Today:<br/><br/>"+table+"<br/><br/> Best Regards,<br/>Money Manager team";
emailService.sendEmail(profile.getEmail(),"Your Daily Expense Summary ",body);
}
log.info("Job Completed:SendDailyExpenseSummary");

    }
}


}
